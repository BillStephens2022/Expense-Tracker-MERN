const { Schema, model } = require('mongoose');

const testimonialSchema = new Schema(
    {
        userId: {
            type: String,
            required: true,
        },
        date: {
            type: Date,
            required: true,
        },
        review: {
            type: String,
            required: true,
        }
    }
);
const Testimonials = model("Testimonials", testimonialSchema);

module.exports = Testimonials;